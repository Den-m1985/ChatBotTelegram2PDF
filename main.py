# –ß–∞—Ç –±–æ—Ç –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Ñ–∞–π–ª—ã.
@bot.message_handler(content_types=['document'])
def handle_docs(message):
    """
    —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –ª—é–±–æ–≥–æ —Ç–∏–ø–∞ —Ñ–∞–π–ª–∞ –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä –≤ —É–∫–∞–∑–∞–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
    :type message: object
    """
    try:
        chat_id = message.chat.id
        # –ø–æ–ª—É—á–∞–µ–º –∏–º—è –∏ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ —Ñ–∞–π–ª–∞, —Ç–∞–∫ —á—Ç–æ –±—ã –ø—Ä–æ–Ω–µ—Å—Ç–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–æ –∫–æ–Ω—Ü–∞
        get_object = message.document  # –ø–æ–ª—É—á–∞–µ–º—ã–π –æ–±—ä–µ–∫—Ç
        real_file_name, real_file_extension = os.path.splitext(get_object.file_name)  # –±–æ—Ç –Ω–µ –ø–æ–Ω–∏–º–∞–µ—Ç –∫–∞—Ä—Ç–∏–Ω–∫—É
        file_name = real_file_name.lower()
        file_extension = real_file_extension.lower()
        file_info = bot.get_file(get_object.file_id)
        downloaded_file = bot.download_file(file_info.file_path)
        src = SRC + file_name + '_' + str(chat_id) + '_' + str(datetime.today().strftime('%Y-%m-%d %H-%M-%S'))
        # —Å–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É –≤ –∫–æ—Ç–æ—Ä–æ–π –±—É–¥–µ–º –≤—Ä–µ–º–µ–Ω–Ω–æ —Ä–∞–∑–º–µ—â–∞—Ç—å —Ñ–∞–π–ª, –µ—Å–ª–∏ —Ç–∞–∫–æ–≤–æ–π –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
        bot.reply_to(message, f"–ü–æ–∂–∞–ª—É–π —Å–æ—Ö—Ä–∞–Ω—é {file_name} üòâ")  # –ù—É–∂–Ω–æ –ª–∏ —ç—Ç–æ –ø–∏—Å–∞—Ç—å? –í—ã–≥–ª—è–¥–∏—Ç –ø–µ—Ä–µ–±–æ—Ä–æ–º!
        if not os.path.exists(src):
            os.makedirs(src)
        # —Å–æ–∑–¥–∞–µ–º –ø—É—Ç—å –∫–æ–Ω–µ—á–Ω–æ–≥–æ —Ñ–∞–π–ª–∞ - –¥—É–º–∞—é –Ω–∞–¥–æ –ø–µ—Ä–µ–¥–µ–ª–∞—Ç—å - —ç—Ç–æ –≤—Ä–µ–º–µ–Ω–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç
        local_src = src + '/' + real_file_name + real_file_extension
        # –ø–∏—à–µ–º —Ñ–∞–π–ª –Ω–∞ –¥–∏—Å–∫
        with open(local_src, 'wb') as new_file:
            new_file.write(downloaded_file)
        if file_extension == '.txt':  # –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ txt
            bot.reply_to(message, "–ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É—é üòâ")  # –ù—É–∂–Ω–æ –ª–∏ —ç—Ç–æ –ø–∏—Å–∞—Ç—å? –í—ã–≥–ª—è–¥–∏—Ç –ø–µ—Ä–µ–±–æ—Ä–æ–º! –ü—Ä–µ–¥–ª–∞–≥–∞—é –º–∏–Ω–∏–º–∞–ª–∏–∑–º
            convert_text_pdf(local_src)  # –∏ –≤–µ—Å—å –ª–∏—à–Ω–∏–π –≤—ã–≤–æ–¥ –≤–æ–æ–±—â–µ —É–±—Ä–∞—Ç—å. –û—Å—Ç–∞–≤–∏–º –ø–æ–∫–∞ —Ç–∞–∫, –¥–ª—è –Ω–∞–≥–ª—è–¥–Ω–æ—Å—Ç–∏.
            send_document(convert_text_pdf(local_src), chat_id)
        elif file_extension == '.xls' \
                or file_extension == '.xlsx':  # –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ excel
            bot.reply_to(message, "xls")
        elif file_extension == '.doc' \
                or file_extension == '.docx':  # –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ doc
            bot.reply_to(message, "doc")
        elif file_extension == '.jpg' or \
                file_extension == '.jpeg' or \
                file_extension == '.png' or \
                file_extension == '.tiff' or \
                file_extension == '.jpg2' or \
                file_extension == '.heif' or \
                file_extension == '.heic':  # –∫–∞—Ä—Ç–∏–Ω–æ–∫
            # –æ—Ç—Å—ã–ª–∞–µ–º —Ñ–∞–π–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é (–∏—Å–ø–æ–ª—å–∑—É–µ–º –º–æ–¥—É–ª—å –∫–æ–Ω–≤–µ—Ä—Ç–µ—Ä–∞)
            bot.reply_to(message, "–ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É—é –∫–∞—Ä—Ç–∏–Ω–∫—É –≤ pdf üòâ")
            img_2_pdf(local_src, message)
            send_document(img_2_pdf(local_src, message), chat_id)
        else:
            bot.reply_to(message, f"—è –Ω–µ –∑–Ω–∞—é —Ç–∞–∫–æ–≥–æ '{file_extension}' —Ñ–æ—Ä–º–∞—Ç–∞ üò∂‚Äçüå´Ô∏èüòá")
        clear_catalog(
            src)  # –í–ù–ò–ú–ê–ù–ò–ï!
        # —Ç–µ–ø–µ—Ä—å —Ñ—É–Ω–∫—Ü–∏—è —É–¥–∞–ª—è–µ—Ç –∏ —Ñ–∞–π–ª—ã –∏ –ø–∞–ø–∫–∏ - –ø—É—Ç–∏ –ø–∏—Å–∞—Ç—å –∞–∫–∫—É—Ä–∞—Ç–Ω–æ —á—Ç–æ –±—ã –Ω–µ –∑–∞—Ç–µ—Ä–ª–æ —Å–∏—Å—Ç–µ–º–Ω—ã–µ —Ñ–∞–π–ª—ã
    except Exception as e:
        bot.reply_to(message, e)


